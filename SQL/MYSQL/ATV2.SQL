USE SOLDADO;

CREATE TABLE ARMAS(
IDARMA INT NOT NULL PRIMARY KEY auto_increment,
NUMERO INT NOT NULL,
TIPO VARCHAR(60) NOT NULL,
CALIBRE FLOAT NOT NULL,
TIPOMUNICAO VARCHAR(60) NOT NULL
);

CREATE TABLE SOLDADO(
RM INT NOT NULL PRIMARY KEY  auto_increment,
NOME VARCHAR(60),
DATANASCIMENTO DATETIME,
IDARMA INT,
foreign key (IDARMA) REFERENCES ARMAS(IDARMA)
);