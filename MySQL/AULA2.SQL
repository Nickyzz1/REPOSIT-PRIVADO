CREATE DATABASE HOSPITAL;

USE HOSPITAL;

CREATE TABLE MEDICOS(
CRM VARCHAR(60) PRIMARY KEY,
NOME VARCHAR(60),
ESPECIALIDADE varchar(60)
);

CREATE TABLE PACIENTES(
COD INT PRIMARY KEY,
NOME VARCHAR(60),
ENDEREÇO VARCHAR(255),
IDADE INT
);

CREATE TABLE MEDICAMENTOS(
IDMEDICAMENTO INT PRIMARY KEY,
TIPO VARCHAR(70),
BULAELETRONICA VARCHAR(255),
DESCRICAO VARCHAR(255),
CONTRAINDICACAO VARCHAR(255)
);

CREATE TABLE EXAMES(
IDEXAME INT PRIMARY KEY,
TIPO VARCHAR(70),
DESCRICAO VARCHAR(255),
CONTRAINDICACAO VARCHAR(255)
);

CREATE TABLE CONSULTA(
IDCONSULTA INT PRIMARY KEY auto_increment,
IDMEDICO VARCHAR(60),
COD INT,
IDMEDICAMENTO INT,
IDEXAME int,
FOREIGN KEY (IDMEDICO) REFERENCES MEDICOS(CRM),
FOREIGN KEY (COD) REFERENCES PACIENTES(COD),
FOREIGN KEY (IDMEDICAMENTO) REFERENCES MEDICAMENTOS(IDMEDICAMENTO),
FOREIGN KEY (IDEXAME) REFERENCES EXAMES(IDEXAME)
);

